public class OperadoresUnarios {
    public static void main(String[] args) {
        int n = 5;
        System.out.println("n = " + n);
        System.out.println("-n = " + (-n));
        System.out.println("+n = " + (+n));
        boolean b = true;
        System.out.println("b = " + b);
        System.out.println("!b = " + !b);
        boolean p = true, q = false;
        System.out.println("p && q: " + (p && q));
        System.out.println("p || q: " + (p || q));
    }
}
