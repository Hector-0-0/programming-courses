public class OperadoresAritmeticosCombinados {
    public static void main(String[] args) {
        int n = 10;

        n += 5;  System.out.println("n += 5  → " + n);
        n -= 3;  System.out.println("n -= 3  → " + n);
        n *= 2;  System.out.println("n *= 2  → " + n);
        n /= 4;  System.out.println("n /= 4  → " + n);
        n %= 3;  System.out.println("n %= 3  → " + n);
    }
}
