public class EjemploIf {
    public static void main(String[] args) {
        int edad = 18;

        if (edad >= 18) {
            System.out.println("Eres mayor de edad");
        }

        int nota = 85;
        if (nota >= 60) {
            System.out.println("Aprobado con nota: " + nota);
        }
    }
}
